# TQC+ 程式語言Python 103 字串格式化輸出
# 設計說明：
# 請撰寫一程式，輸入四個單字，然後將這四個單字以欄寬為10、欄與欄間隔一個空白字元、
# 每列印兩個的方式，先列印向右靠齊，再列印向左靠齊，左右皆以直線 |（Vertical bar）作為邊界。
w1=input()
w2=input()
w3=input()
w4=input()
print(f"|{w1:>10} {w2:>10}|")
print(f"|{w3:>10} {w4:>10}|")
print(f"|{w1:<10} {w2:<10}|")
print(f"|{w3:<10} {w4:<10}|")
print("|%10s %10s|" %(w1, w2))
print("|%10s %10s|" %(w3, w4))
print("|%-10s %-10s|" %(w1, w2))
print("|%-10s %-10s|" %(w3, w4))